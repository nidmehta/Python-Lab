#Python doesn't enforce any access specifier but there is a common syntax that everyone follows for ease
#Public- var
#Protected- _var     use in current as well as derived class
#Private- __var      only use in current class

#For accessing private members- _Classnname__var
#This is called name mangling or name decoration
#Stored in python like that so that the user doesn't use it out of scope by mistake

